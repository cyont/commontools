		<a name="editR"></a>
		<p><strong>Edit a Resource</strong> (<a href="#top">top</a>)<br />
		Once a resource has been uploaded AND assigned to a display block, several of its fields may be edited. Navigate to the edit screen by clicking on its linked name below the image in the display block editor.</p>

		<a name="replace"></a>
		<p><strong>Replace a Resource</strong> (<a href="#top">top</a>)<br />
		An existing resource may be swapped with a different image while retaining all of its existing information. From the "Edit a Resource" page, click the "Replace Resource" link, make any edits to the name, alt text, etc., and then upload the replacement image. Scheduling for the resource remains unchanged.</p>

		<a name="editD"></a>
		<p><strong>Edit a Display Block</strong> (<a href="#top">top</a>)<br />
		Display blocks may be edited by clicking "View active display blocks" link from the main page of Deliverance. Choose the display block you want to edit and click its linked name in the "Name" column. From the "Edit a Display Block" page, a number of options are available:</p>
		
		<ul class="help">
			<li><em><strong>Left Column</strong></em><br />
			This area displays a list of resources currently assigned to the display block. Below each resource image is the resource name. It links to the "Edit a Resource" page. The "Queue" displays information about scheduled run dates, such as total and remaining time. The linked "X" next to the "Queue" removes the resource from the display block's schedule.</li>
			
			<li><em><strong>Right Column</strong></em><br />
			The right column is where the actual scheduling happens. Use the "Choose a resource" pulldown to select from the resources uploaded within the past 30 days. Older resources may be located using the search option. See below for more information. Once the desired resource has been located, use the calendar popups to schedule the start/end dates. Then click the "Assign Resource" button to complete the scheduling.</li>
			
			<li><em><strong>Bottom Row/Current Schedule</strong></em><br />
			This area provides a week-at-a-glance view of all resources running in the display block. Click the links below the calendar to view past and future schedules.</li>
		</ul>
		
		<a name="search"></a>
		<p><strong>Search Resources</strong> (<a href="#top">top</a>)<br />
		To find resources uploaded more than 30 days ago, click the magnifying glass icon next to "Resources & Run Dates" in the right column of the display block editor. The search page allows searching by resource name (or part of a name) as well as specifying an age limit. Once the desired resource has been located, click the "Select" button to the right of the resource to return the the main display block page and complete the scheduling process.</p>

		<a name="updates"></a>
		<p><strong>Run Updates</strong> (<a href="#top">top</a>)<br />
		Deliverance updates schedules daily at 2 a.m. The updates are processor/server intensive due to the number of resources being served from the Deliverance system, so every effort should be made to schedule resources at least one day in advance. In the event that a change must go live immediately, the "Run Updates" link from the main page should be used. This option is only available for use two times/day. Please plan ahead and use this option only if absolutely necessary.</p>

